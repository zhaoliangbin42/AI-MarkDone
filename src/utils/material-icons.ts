/**
 * Create a Material Icon SVG element
 * @param iconName Icon name from MaterialIcons
 * @param size Icon size in pixels (default: 24)
 * @param color Icon color (default: currentColor)
 * @returns SVG element
 */
export function createMaterialIcon(
    iconName: keyof typeof MaterialIcons,
    size: number = 24,
    color: string = 'currentColor'
): SVGElement {
    const svgString = MaterialIcons[iconName];
    const template = document.createElement('template');
    template.innerHTML = svgString.trim();
    const svg = template.content.firstElementChild as SVGElement | null;

    if (svg) {
        svg.setAttribute('width', size.toString());
        svg.setAttribute('height', size.toString());
        svg.setAttribute('fill', color);
        svg.style.display = 'block';
        svg.style.flexShrink = '0';
        return svg;
    }

    const fallback = document.createElementNS('http://www.w3.org/2000/svg', 'svg');
    fallback.setAttribute('width', size.toString());
    fallback.setAttribute('height', size.toString());
    fallback.setAttribute('viewBox', '0 0 24 24');
    fallback.style.display = 'block';
    fallback.style.flexShrink = '0';
    return fallback;
}
